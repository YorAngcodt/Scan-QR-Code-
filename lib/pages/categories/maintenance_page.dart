                final created = await Navigator.of(context).push<bool>(
                  MaterialPageRoute(
                    builder: (_) => MaintenanceCreatePage(
                      assetId: widget.assetId,
                      assetName: null,
                    ),
                  ),
                );
                if (created == true) {
                  await _load();
                }
              },
              backgroundColor: Theme.of(context).primaryColor,
              child: const Icon(Icons.add, color: Colors.white),
            )
          : null,
    );
  }

  List<MaintenanceRequest> _applyFilters() {
    final q = _query.trim().toLowerCase();
    return _items.where((m) {
      final matchesQuickSearch = q.isEmpty ||
          m.title.toLowerCase().contains(q) ||
          (m.assetName ?? '').toLowerCase().contains(q) ||
          (m.assetCode ?? '').toLowerCase().contains(q) ||
          (m.categoryName ?? '').toLowerCase().contains(q) ||
          (m.locationName ?? '').toLowerCase().contains(q);

      final matchesAssetName = _matchesFilter(m.assetName, _filterAssetName);
      final matchesCategory = _matchesFilter(m.categoryName, _filterCategory);
      final matchesLocation = _matchesFilter(m.locationName, _filterLocation);
      final matchesSerial = _matchesFilter(m.assetCode, _filterSerial);
      final matchesResponsible = _matchesFilter(m.responsiblePersonName, _filterResponsible);
      final matchesStatus = _matchesFilter(m.state, _filterStatus);

      final pattern = m.assetRecurrencePattern;
      final startDate = _formatDate(m.assetRecurrenceStartDate);
      final interval = m.assetRecurrenceInterval?.toString();
      final endDate = _formatDate(m.assetRecurrenceEndDate);

      final matchesPattern = _matchesFilter(pattern, _filterPattern);
      final matchesStartDate = _matchesFilter(startDate, _filterStartDate);
      final matchesInterval = _matchesFilter(interval, _filterInterval);
      final matchesEndDate = _matchesFilter(endDate, _filterEndDate);

      return matchesQuickSearch &&
          matchesAssetName &&
          matchesCategory &&
          matchesLocation &&
          matchesSerial &&
          matchesResponsible &&
          matchesStatus &&
          matchesPattern &&
          matchesStartDate &&
          matchesInterval &&
          matchesEndDate;
    }).toList();
  }

  bool _matchesFilter(String? source, String? filter) {
    final trimmed = filter?.trim();
    if (trimmed == null || trimmed.isEmpty) return true;
    return (source ?? '').toLowerCase().contains(trimmed.toLowerCase());
  }

  Future<void> _openFilterDialog() async {
    setState(() {
      _filterSheetOpen = true;
