import 'package:flutter/material.dart';
import 'dart:async';
import '../../models/maintenance_request.dart';
import '../../services/api_service.dart';
import '../../services/auth_service.dart';
import '../maintenance_detail_page.dart';
import '../maintenance_create_page.dart';

class MaintenancePage extends StatefulWidget {
  final int? assetId;
  final bool autoGeneratedOnly;
  final bool showTitle;

  const MaintenancePage({super.key, this.assetId, this.autoGeneratedOnly = false, this.showTitle = false});

  @override
  State<MaintenancePage> createState() => _MaintenancePageState();
}

class _MaintenancePageState extends State<MaintenancePage> {
  final TextEditingController _search = TextEditingController();
  Timer? _debounce;
  String _query = '';
  bool _loading = false;
  String? _error;
  List<MaintenanceRequest> _items = [];
  bool _grid = true;
  bool _canManageMaintenance = false;
  bool _filterSheetOpen = false;
  String? _filterAssetName;
  String? _filterCategory;
  String? _filterLocation;
  String? _filterSerial;
  String? _filterResponsible;
  String? _filterStatus;
  String? _filterPattern;
  String? _filterStartDate;
  String? _filterInterval;
  String? _filterEndDate;
  List<String> _assetNameOptions = [];
  List<String> _categoryOptions = [];
  List<String> _locationOptions = [];
  List<String> _serialOptions = [];
  List<String> _responsibleOptions = [];
  List<String> _statusOptions = [];
  List<String> _patternOptions = [];
  List<String> _startDateOptions = [];
  List<String> _intervalOptions = [];
  List<String> _endDateOptions = [];

  @override
  void initState() {
    super.initState();
    _loadPermissions();
    _load();
  }

  @override
  void dispose() {
    _debounce?.cancel();
    _search.dispose();
    super.dispose();
  }

  Future<void> _load() async {
    setState(() { _loading = true; _error = null; });
    try {
      final res = await ApiService.fetchMaintenanceRequests(
        query: _query,
        assetId: widget.assetId,
        autoGeneratedOnly: widget.autoGeneratedOnly,
      );
      if (!mounted) return;
      setState(() {
        _items = res;
        _assetNameOptions = _buildOptions(res.map((m) => m.assetName));
        _categoryOptions = _buildOptions(res.map((m) => m.categoryName));
        _locationOptions = _buildOptions(res.map((m) => m.locationName));
        _serialOptions = _buildOptions(res.map((m) => m.assetCode));
        _responsibleOptions = _buildOptions(res.map((m) => m.responsiblePersonName));
        _statusOptions = _buildOptions(res.map((m) => m.state));
        _patternOptions = _buildOptions(res.map((m) => m.assetRecurrencePattern));
        _startDateOptions = _buildOptions(res.map((m) => _formatDate(m.assetRecurrenceStartDate)));
        _intervalOptions = _buildOptions(res.map((m) => m.assetRecurrenceInterval?.toString()));
        _endDateOptions = _buildOptions(res.map((m) => _formatDate(m.assetRecurrenceEndDate)));
      });
    } catch (e) {
      if (!mounted) return;
      setState(() { _error = e.toString(); });
    } finally {
      if (mounted) setState(() { _loading = false; });
    }
  }

  Future<void> _loadPermissions() async {
    // 1) Gunakan role yang tersimpan di lokal terlebih dahulu
    try {
      final cachedRole = await AuthService.getUserRole();
      if (cachedRole != null && cachedRole.isNotEmpty && mounted) {
        final canManage = cachedRole == 'Manager' || cachedRole == 'Team';
        setState(() {
          _canManageMaintenance = canManage;
        });
      }
    } catch (_) {}

    // 2) Refresh dari API, sekaligus memastikan role terbaru
    try {
      final info = await ApiService.fetchCurrentUserInfo();
      if (!mounted) return;
      final role = (info['role'] ?? '').toString();
      final canManage = role == 'Manager' || role == 'Team';
      setState(() {
        _canManageMaintenance = canManage;
      });
    } catch (_) {
      // Biarkan pakai cached role jika ada
    }
  }

  Widget _buildPriorityStars(String? priority) {
    int p = int.tryParse(priority ?? '') ?? 0;
    if (p < 0) p = 0;
    if (p > 3) p = 3;
    return Row(
      mainAxisSize: MainAxisSize.min,
      children: List.generate(3, (index) {
        final selected = index < p;
        return Icon(
          Icons.star,
          size: 14,
          color: selected ? Colors.amber : Colors.grey.shade300,
        );
      }),
    );
  }

  Color _stateColor(String s) {
    switch (s.toLowerCase()) {
      case 'done':
        return const Color(0xFF16A34A);
      case 'repaired':
        return const Color(0xFFDC2626);
      case 'in_progress':
        return const Color(0xFFF59E0B);
      case 'cancelled':
      case 'cancel':
        return const Color(0xFFEF4444);
      case 'draft':
        return const Color(0xFF6B7280);
      default:
        return const Color(0xFF6B7280);
    }
  }

